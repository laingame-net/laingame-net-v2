        <td width="20%">How to “play” instructions</td>
        <td>
          The audio and video files are organized in two parts: “Site A” and
          “Site B”, they are represent each of two CD disks of original game.
          Site A contains 22 levels, Site B contains 13 levels. Each level
          contains different numbers of files. Actually, this game has no
          gameplay. The player must check each file one by one from bottom
          levels to the upper and assemble storylines from pieces of
          information. Instead of original game, you can see and open all
          files in any order. However, be careful, you can easily running down
          the one storyline, and falling behind in another storyline. Links on
          unvisited pages is highlighted. You must always check “Depends on”
          links at the file page, if you see yellow links there, you must
          check them out before continue current file.
        </td>
