        <td width="20%">About the game</td>
        <td>
          On November 26, 1998, Pioneer LDC released a video game for the
          PlayStation with the same name as the Serial Experiments Lain anime.
          It was designed by Konaka and Yasuyuki and made to be a "network
          simulator" in which the player would navigate to explore Lain's
          story. The creators themselves did not call it a game, but
          "Psycho-Stretch-Ware", and it has been described as being a kind of
          graphic novel: the gameplay is limited to unlocking pieces of
          information, and then reading/viewing/listening to them, with little
          or no puzzle needed to unlock. Read more on
          <a href="https://en.wikipedia.org/wiki/Serial_Experiments_Lain#Video_game">Wikipedia</a>
          and
          <a href="https://lain.wiki/wiki/Serial_Experiments_Lain_(game)">lain.wiki</a>
